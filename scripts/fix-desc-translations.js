// Helper: word-boundary safe replace (handles accented chars by using space/dash boundaries)
function wb(str) {
  // Escape special regex chars, then wrap in \b word boundaries.
  // Works for ASCII strings; for accented chars \b still guards against
  // mid-word matches because the char before/after the accented word is
  // typically a space or punctuation (non-word char).
  const escaped = str.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
  return new RegExp('\\b' + escaped + '\\b', 'gi');
}

// Helper for words starting/ending with accented chars (where \b doesn't work)
function wba(str) {
  const escaped = str.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
  return new RegExp('(?<![a-zA-Z0-9_])' + escaped + '(?![a-zA-Z0-9_])', 'gi');
}

